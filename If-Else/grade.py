m = int(input())
if m>80:
    print("A+")
elif m>=75 and m<80:
    print("A")
elif m>=70 and m<75:
    print("A-")
elif m>=65 and m<70:
    print("B+")
elif m>=60 and m<65:
    print("B")
elif m>=55 and m<60:
    print("B-")
elif m>=50 and m<55:
    print("C+")
elif m>=45 and m<50:
    print("C")
elif m>=40 and m<45:
    print("D")
else:
    print("F")